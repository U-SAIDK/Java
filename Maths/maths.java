package Maths;

public class maths {
    static void main(String[] args) {

        System.out.println(Math.PI); // Print the Value of Pi
                                     // Math here is Class

        double result;
        result = Math.pow(2,3); // 2 to the power(pow) of 8
        double result1 = Math.abs(-5); // Absolute value
        double result2 = Math.sqrt(9);
        double result3 = Math.round(3.15);
        double result4 = Math.max(10, 20);
        double result5 = Math.min(10, 20);
        System.out.println(result);
        System.out.println(result1);
        System.out.println(result2);
        System.out.println(result3);
        System.out.println(result4);
        System.out.println(result5);


    }
}
